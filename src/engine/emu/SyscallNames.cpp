const char* GetSyscallName(uint64_t syscallNumber)
{
    static const size_t tableSize = sizeof(g_syscallTable) / sizeof(g_syscallTable[0]);

    for (size_t i = 0; i < tableSize; i++) {
        if (g_syscallTable[i].number == syscallNumber) {
            return g_syscallTable[i].name;
        }
    }
    return "Unknown";
}

uint64_t GetSyscallCount()
{
    return sizeof(g_syscallTable) / sizeof(g_syscallTable[0]);
}
